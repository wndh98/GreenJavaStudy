package variable;

public class Var04 {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
//		int x = 10, y = 20;
		System.out.println("X : " + x + "| Y : " + y);
		x = y; // x가 20으로 치환됨
		y = x; // y에 x 값 넣음
		System.out.println("X : " + x + "| Y : " + y);
		x = 10;
		System.out.println("X : " + x + "| Y : " + y);
		// 값교환 임시 변수를 사용
		int temp;
		temp = x;
		x = y;
		y = temp;
		System.out.println("X : " + x + "| Y : " + y);

	}

}
